function init() {
  let params = new URL(document.location).searchParams;
  let item = params.get("item");

  if (searchForComponent(item)) {
    shoppingList(item);
  } else {
    index();
  }
}

// data logic

function searchForComponent(item, data = craftingData) {
  var found = data.find((product, index) => product.name == item);
  return found;
}

// Presentation section:

// Pages

function index(data = craftingData) {
  var headings = ["<b>Item</b>", "<b>Value</b>"];
  addTableHeading(headings);

  data.forEach( row => {
    var {name, value} = row;
    var cells = [`<a href="/?item=${name}">${name}</a>`, numberWithCommas(value)];
    addTableRow(cells);
  });
}

function shoppingList(item) {
  var found = searchForComponent(item);
  displayElement(`${item}`, "h1");
  displayElement(`${numberWithCommas(found.value)}`, "p.units");
  if(filterOnCraftable(found.resources).length > 0) {
    displayElement("Craftable Components", "h2");

    // Craftable shopping list...
    let componentTable = displayResourcesTable(null, document.body, ["Component", "Quantity", "Cost"]);
    showResources(found.resources, componentTable);
  }
  displayElement(`<a name="mined_and_cultivated"></a>`);

  showMinedAndCultivatedMaterials(item);
}

function showResources(resources, table) {
  resources.forEach(
    (resource) => {
      var found = searchForComponent(resource.name);
      if(found) {
        let value = searchForComponent(resource.name)?.value;
        addTableRow([`<a href="/?item=${resource.name}">${resource.name}</a>`, resource.qty, numberWithCommas(value)], table);
        showResources(found.resources, table);
      };
    }
  );
}

function getResources(resources, collection = []) {
  resources?.forEach(
    (resource) => {
      collection.push(resource);
      var found = searchForComponent(resource?.name);
      return getResources(found?.resources, collection);
    }
  );

  return collection;
}

function sortResources(a, b) {
  return a.name < b.name ? -1 : 1;
}

function aggregatedResouces(resources) {
  return resources.reduce((p, c) => {

    // Check for exisiting resource and add to it.
    let i = p?.findIndex( e => e.name === c.name );

    if(i > -1) {
      p[i].qty += c.qty;
      return p;
    }

    p?.push(c);
    return p;
  }, []);
}

function filterOnMinedAndCultivated(resources) {
  return resources.filter( r => minedAndCultivated.map( e => e.name ).includes(r.name) );
}

function filterOnCraftable(resources) {
  return resources.filter( r => !minedAndCultivated.map( e => e.name ).includes(r.name) );
}

function getSortedResources(item) {
  return getResources(searchForComponent(item).resources).sort(sortResources);
}

function addCostToMinedAndCultivatedResources(resources) {
  return resources.map( r => {
    r.cost = minedAndCultivated.find( e => r.name == e.name ).value * r.qty;
    return r;
  });
}

function showMinedAndCultivatedMaterials(item){
  displayElement(`<a name="mined_and_cultivated"></a>Mined and cultivated materials`, "h2");
  let transformedResources =
      addCostToMinedAndCultivatedResources(
        filterOnMinedAndCultivated(
          aggregatedResouces(
            getSortedResources(item))));

  let totalCost = transformedResources.reduce( (p, c) => {
    return c.cost + p;
  }, 0);

  let profit =  searchForComponent(item).value - totalCost;

  displayResourcesTable(
    transformedResources.concat(
      [
        {name: "<b>Total Cost</b>", qty: "", cost: `<b>${numberWithCommas(totalCost)}</b>`},
        {name: "<b>Profit</b>", qty: "", cost: `<b>${numberWithCommas(profit)}</b`}
      ]
    ));
}

// html helpers

function displayElement(text, htmlElementName = "p", container = document.body) {
  var className;

  if(htmlElementName.includes('.')) {
    let elementParts = htmlElementName.split(".");
    htmlElementName = elementParts[0];
    className = elementParts[1];
  }

  var element = document.createElement(htmlElementName);
  element.innerHTML = text;

  if (className !== undefined) {
    element.className = className;
  }

  container.appendChild(element);
  return element;
}

function displayHeading(text, container = document.body) {
  return displayElement(text, "h1", container);
}

function displayResourcesTable(items, container = document.body, headings = ["Component", "Quantity", "Cost"]) {
  var table = document.createElement("table");
  container.appendChild(table);

  addTableHeading(headings.map( it => `<b>${it}</b>` ), table);

  items?.forEach((i) => addTableRow([i.name, i.qty, numberWithCommas(i.cost)], table));

  return table;
}

function addTableHeading(cells, table = document.getElementById("crafting")) {
  var thead = document.createElement("thead");
  var tr = document.createElement("tr");

  cells.forEach((c) => {
    var th = document.createElement("th");
    th.innerHTML = c;
    tr.appendChild(th);
  });

  thead.appendChild(tr);
  table.appendChild(thead);
}

function addTableRow(cells, table = document.getElementById("crafting")) {
  var tr = document.createElement("tr");

  cells.forEach((c) => {
    var td = document.createElement("td");
    td.innerHTML = c;
    tr.appendChild(td);
  });

  table.appendChild(tr);
}

function numberWithCommas(x) {
  return x.toString().replace(/\B(?=(\d{3})+(?!\d))/g, ",");
}
