function init() {
  let params = new URL(document.location).searchParams;
  let item = params.get("item");

  if (searchFor(item)) {
    shoppingList(item);
  } else {
    index();
  }
}

// data logic

function searchFor(item, data = craftingData) {
  var found = data.find((product, index) => product.name == item);
  return found;
}

// Presentation section:

// Pages

function index(data = craftingData) {
  var headings = ["<b>Item</b>", "<b>Value</b>"];
  addTableHeading(headings);

  data.forEach( row => {
    var {name, value} = row;
    var cells = [`<a href="/?item=${name}">${name}</a>`, value];
    addTableRow(cells);
  });
}

function shoppingList(item) {
  var found = searchFor(item);
  displayElement(`${item}: ${found.value}u`, "h1");
  displayElement("Shopping List...", "h2");
  if(filterOnCraftable(found.resources).length > 0) {

  // Craftable shopping list...
    let componentTable = displayResourcesTable(null, document.body, "Component", "Quantity");
    showResources(found.resources, componentTable);
  }
  displayElement(`<a name="bom"></a>`);

  showBillOfMaterials(item);
}

function showResources(resources, table) {
  resources.forEach(
    (resource) => {
      var found = searchFor(resource.name);
      if(found) {
        addTableRow([`<a href="/?item=${resource.name}">${resource.name}</a>`, resource.qty], table);
        showResources(found.resources, table);
      };
    }
  );
}

function getResources(resources, collection = []) {
  resources?.forEach(
    (resource) => {
        collection.push(resource);
        var found = searchFor(resource?.name);
        return getResources(found?.resources, collection);
    }
  );

  return collection;
}

function sortResouces(a, b) {
  return a.name < b.name ? -1 : 1;
}

function reducedResouces(resources) {
  return resources.reduce((p, c) => {

    // Check for exisiting resource and add to it.
    let i = p?.findIndex( e => e.name === c.name );

    if(i > -1) {
      p[i].qty += c.qty;
      return p;
    }

    p?.push(c);
    return p;
  }, []);
}

function filterOnMinedAndCultivated(resources) {
  return resources.filter( r => minedAndCultivated.includes(r.name) );
}

function filterOnCraftable(resources) {
  return resources.filter( r => !minedAndCultivated.includes(r.name) );
}

function getSortedResources(item) {
  return getResources(searchFor(item).resources).sort(sortResouces);
}

function showBillOfMaterials(item){
  displayElement(`<a name="bom"></a> ${item} - Bill of Materials`, "h2");
  displayResourcesTable(filterOnMinedAndCultivated(reducedResouces(getSortedResources(item))));
}

// html helpers
function displayElement(text, htmlElementName = "p", container = document.body) {
  var element = document.createElement(htmlElementName);
  element.innerHTML = text;
  container.appendChild(element);
  return element;
}

function displayHeading(text, container = document.body) {
  return displayElement(text, "h1", container);
}

function displayResourcesTable(items, container = document.body, column1Heading = "Component", column2Heading = "Quantity") {
  var table = document.createElement("table");
  container.appendChild(table);

  addTableHeading([`<b>${column1Heading}</b>`, `<b>${column2Heading}</b>`], table);

  items?.forEach((i) => addTableRow([i.name, i.qty], table));

  return table;
}

function addTableHeading(cells, table = document.getElementById("crafting")) {
  var thead = document.createElement("thead");
  var tr = document.createElement("tr");

  cells.forEach((c) => {
    var th = document.createElement("th");
    th.innerHTML = c;
    tr.appendChild(th);
  });

  thead.appendChild(tr);
  table.appendChild(thead);
}

function addTableRow(cells, table = document.getElementById("crafting")) {
  var tr = document.createElement("tr");

  cells.forEach((c) => {
    var td = document.createElement("td");
    td.innerHTML = c;
    tr.appendChild(td);
  });

  table.appendChild(tr);
}
